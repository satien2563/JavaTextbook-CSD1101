package chap03_io;

public class AssignmentExample {
    public static void main(String[] args) {
        int x = 10;

        x += 5;   // x = 15
        x -= 3;   // x = 12
        x *= 2;   // x = 24
        x /= 4;   // x = 6
        x %= 4;   // x = 2

        System.out.println("ค่าของ x สุดท้ายคือ: " + x);
    }

}
